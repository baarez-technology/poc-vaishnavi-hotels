// Helper function to get booking by ID
export function getBookingById(id) {
  return bookingsData.find((booking) => booking.id === id);
}

// Helper function to filter bookings by status
export function filterByStatus(bookings, status) {
  if (!status || status === 'all') return bookings;
  return bookings.filter((booking) => booking.status === status);
}

// Helper function to filter bookings by source
export function filterBySource(bookings, source) {
  if (!source || source === 'all') return bookings;
  return bookings.filter((booking) => booking.source === source);
}

// Helper function to filter bookings by date range
export function filterByDateRange(bookings, startDate, endDate) {
  if (!startDate && !endDate) return bookings;

  return bookings.filter((booking) => {
    const checkIn = new Date(booking.checkIn);
    const start = startDate ? new Date(startDate) : new Date('1900-01-01');
    const end = endDate ? new Date(endDate) : new Date('2100-12-31');

    return checkIn >= start && checkIn <= end;
  });
}

// Helper function to get arrivals for today
export function getArrivalsToday(bookings) {
  const today = new Date().toISOString().split('T')[0];
  return bookings.filter((booking) => booking.checkIn === today);
}

// Helper function to get departures for today
export function getDeparturesToday(bookings) {
  const today = new Date().toISOString().split('T')[0];
  return bookings.filter((booking) => booking.checkOut === today);
}

// Helper function for fuzzy search
export function searchBookings(bookings, query) {
  if (!query || query.trim() === '') return bookings;

  const searchTerm = query.toLowerCase().trim();

  return bookings.filter((booking) => {
    return (
      booking.id.toLowerCase().includes(searchTerm) ||
      booking.guest.toLowerCase().includes(searchTerm) ||
      booking.room.toLowerCase().includes(searchTerm) ||
      booking.roomType.toLowerCase().includes(searchTerm) ||
      booking.email.toLowerCase().includes(searchTerm)
    );
  });
}

